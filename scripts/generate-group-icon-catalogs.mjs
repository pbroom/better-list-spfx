import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const outputRoot = path.join(root, 'src/webparts/betterList/components/generated/group-icons');
const shardCount = 16;
const sizePreference = [24, 20, 16, 28, 32, 40, 48, 12];

const libraries = [
  {
    id: 'solar-duotone',
    packageName: 'solar',
    select(names) {
      return names.filter((name) => name.endsWith('-bold-duotone'));
    }
  },
  {
    id: 'fluent',
    packageName: 'fluent',
    select(names) {
      return selectCanonicalSize(names, /^(.*)-(12|16|20|24|28|32|40|48)-regular$/);
    }
  },
  {
    id: 'fluent-color',
    packageName: 'fluent-color',
    select(names) {
      return selectCanonicalSize(names, /^(.*)-(16|20|24|28|32|48)$/);
    }
  }
];

await fs.rm(outputRoot, { force: true, recursive: true });
await fs.mkdir(outputRoot, { recursive: true });

for (const library of libraries) {
  const sourcePath = path.join(root, 'node_modules', '@iconify-json', library.packageName, 'icons.json');
  const source = JSON.parse(await fs.readFile(sourcePath, 'utf8'));
  const names = library.select(Object.keys(source.icons)).sort((left, right) => left.localeCompare(right));
  const shards = Array.from({ length: shardCount }, () => ({}));
  const manifest = names.map((name) => {
    const shard = getShard(name);
    const icon = source.icons[name];
    shards[shard][name] = {
      ...icon,
      width: icon.width ?? source.width ?? 16,
      height: icon.height ?? source.height ?? 16
    };
    const label = humanize(name, library.id);
    return { name, label, searchText: `${label} ${name}`.toLocaleLowerCase(), shard };
  });

  const directory = path.join(outputRoot, library.id);
  await fs.mkdir(directory, { recursive: true });
  await fs.writeFile(
    path.join(directory, 'manifest.ts'),
    generatedModule('manifest', manifest),
    'utf8'
  );
  await Promise.all(shards.map((icons, index) =>
    fs.writeFile(
      path.join(directory, `shard-${String(index).padStart(2, '0')}.ts`),
      generatedModule('icons', icons),
      'utf8'
    )
  ));
  process.stdout.write(`${library.id}: ${names.length} icons in ${shardCount} shards\n`);
}

await fs.writeFile(path.join(outputRoot, 'loaders.ts'), generatedLoaders(), 'utf8');

function selectCanonicalSize(names, pattern) {
  const grouped = new Map();
  for (const name of names) {
    const match = name.match(pattern);
    if (!match) continue;
    const candidates = grouped.get(match[1]) || [];
    candidates.push({ name, size: Number(match[2]) });
    grouped.set(match[1], candidates);
  }
  return [...grouped.values()].map((candidates) =>
    candidates.sort((left, right) => sizePreference.indexOf(left.size) - sizePreference.indexOf(right.size))[0].name
  );
}

function getShard(name) {
  const firstLetter = name.charCodeAt(0) - 97;
  return Math.max(0, Math.min(shardCount - 1, Math.floor(firstLetter * shardCount / 26)));
}

function humanize(name, library) {
  const withoutVariant = library === 'solar-duotone'
    ? name.replace(/-bold-duotone$/, '')
    : name.replace(/-(?:12|16|20|24|28|32|40|48)(?:-regular)?$/, '');
  return withoutVariant
    .split('-')
    .filter(Boolean)
    .map((word) => word === 'ui' ? 'UI' : word === 'qr' ? 'QR' : word.charAt(0).toUpperCase() + word.slice(1))
    .join(' ');
}

function generatedModule(name, value) {
  return `// Generated by scripts/generate-group-icon-catalogs.mjs. Do not edit.\n` +
    `const ${name} = ${JSON.stringify(value)} as const;\n\nexport default ${name};\n`;
}

function generatedLoaders() {
  const manifestCases = libraries.map(({ id }) =>
    `    case '${id}': return import(/* webpackChunkName: "group-icons-${id}-manifest" */ './${id}/manifest');`
  ).join('\n');
  const shardCases = libraries.map(({ id }) => {
    const imports = Array.from({ length: shardCount }, (_, index) => {
      const padded = String(index).padStart(2, '0');
      return `      () => import(/* webpackChunkName: "group-icons-${id}-${padded}" */ './${id}/shard-${padded}')`;
    }).join(',\n');
    return `    case '${id}':\n      return [\n${imports}\n      ];`;
  }).join('\n');
  return `// Generated by scripts/generate-group-icon-catalogs.mjs. Do not edit.\n` +
    `import { BetterListGroupIconLibrary } from '../../../../../shared';\n\n` +
    `export function loadGeneratedGroupIconManifest(library: BetterListGroupIconLibrary): Promise<{ default: readonly unknown[] }> {\n` +
    `  switch (library) {\n${manifestCases}\n  }\n}\n\n` +
    `export function getGeneratedGroupIconShardLoaders(library: BetterListGroupIconLibrary): readonly (() => Promise<{ default: unknown }>)[] {\n` +
    `  switch (library) {\n${shardCases}\n  }\n}\n`;
}
